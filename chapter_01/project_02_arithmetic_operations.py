x = int(input("Provide a number: "))
y = int(input("Provide a second number: "))

print(f"The sum of {x} and {y} is {x+y}")
print(f"The difference between {x} and {y} is {x-y}")
print(f"The product of {x} and {y} is {x*y}")
print(f"The division of {x} by {y} is {x/y}")
print(f"The remainder of dividing {x} by {y} is {x % y}")
